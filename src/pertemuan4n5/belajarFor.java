package pertemuan4n5;

public class belajarFor {
    public static void main(String[] args) {
        // syntax: for (inisialisasi; kondisi; perubahan)
        for (int i = 1; i<=10; i++){
            // hal yg di loop dsini
            System.out.println("DIDALAM LOOP");
            System.out.println("i = " + i);
            // i-=1; JANGAN ada perubahan di dalam loop
        }
        // FOR dipakai untuk hal hal yang jumlah iterasinya pasti /
        // tidak tergantung statement didalamnya

    }
}
